    private void btn_ThemGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemGVActionPerformed
        String MaGV_s = txt_MaGV.getText();
        String TenGV = txt_TenGV.getText();
        String TenMH = sl_MH.getSelectedItem().toString();
        String SDTGV =  txt_SDTGV.getText();
        String EMGV = txt_EMGV.getText();
        String DCGV = txt_DCGV.getText();
        String NSGV_s = txt_NSGV.getText();
        if(!"".equals(TenGV) && !"".equals(SDTGV) && !"".equals(EMGV) && !"".equals(DCGV) && !"".equals(NSGV_s) && !"".equals(TenMH)) {
            try {
                int MaGV = Integer.parseInt(txt_MaGV.getText());
                int NSGV = Integer.parseInt(txt_NSGV.getText());
                GiaoVien GV = new GiaoVien();
                GV.addGV(MaGV, TenGV, 0, NSGV, EMGV, SDTGV, DCGV);
                GV.setTenMH(TenMH);
                GiaoVienBLL GVBLL = new GiaoVienBLL();
                if(GVBLL.AddGV(GV)=="") {
                    JOptionPane.showMessageDialog(rootPane, "Đã Thêm Giáo Viên");
                    model.addRow(new Object[] {
                        GV.getMaGV(),
                        GV.getTenGV(),
                        GV.getTenMH(),
                        GV.getNamSinh(),
                        GV.getSDT(),
                        GV.getDiaChi(),
                        GV.getEmail()
                    });
                } else {
                    JOptionPane.showMessageDialog(rootPane, GVBLL.AddGV(GV));
                }
                txt_MaGV.setText("");
                txt_TenGV.setText("");
                txt_SDTGV.setText("");
                txt_EMGV.setText("");
                txt_DCGV.setText("");
                txt_NSGV.setText("");
                sl_TT.setSelectedItem("Còn Hoạt Động");
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(rootPane, "Mã giáo viên phải ở kiểu số nguyên");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đầy đủ thông tin! Mã giáo viên phải lớn hơn 0!");
        }
    }//GEN-LAST:event_btn_ThemGVActionPerformed

    private void btn_SuaGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaGVActionPerformed
        int MaGV_New = Integer.parseInt(txt_MaGV.getText());
        String TenGV = txt_TenGV.getText();
        String TenMH = sl_MH.getSelectedItem().toString();
        String SDTGV =  txt_SDTGV.getText();
        String EMGV = txt_EMGV.getText();
        String DCGV = txt_DCGV.getText();
        String NSGV_s = txt_NSGV.getText();
        String TinhTrang = sl_TT.getSelectedItem().toString();
        if(!"".equals(TenGV) && !"".equals(SDTGV) && !"".equals(EMGV) && !"".equals(DCGV) && !"".equals(NSGV_s) && !"".equals(TenMH)) {
            GiaoVien GV = new GiaoVien();
            GV.addGV(MaGV_New, TenGV, 0, Integer.parseInt(NSGV_s), EMGV, SDTGV, DCGV);
            GV.setTenMH(TenMH);
            int i=0;
            int TT=1;
            if(TinhTrang=="Ngừng Hoạt Động")
            TT=0;
            int this_Row=tbl_GV.getSelectedRow();
            GV.setTinhTrang(TT);
            String MaGV_t = txt_MaGV.getText();
            GiaoVienBLL GVBLL = new GiaoVienBLL();
            if(GVBLL.UpdateGV(GV)=="")
            {
                this.model.setValueAt(MaGV_t, this_Row, 0);
                this.model.setValueAt(TenGV, this_Row, 1);
                this.model.setValueAt(TenMH, this_Row, 2);
                this.model.setValueAt(NSGV_s, this_Row, 3);
                this.model.setValueAt(SDTGV, this_Row, 4);
                this.model.setValueAt(DCGV, this_Row, 5);
                this.model.setValueAt(EMGV, this_Row, 6);
                this.model.setValueAt(TinhTrang, this_Row, 7);
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Sửa Giáo Viên Thất Bại");
            }
            txt_MaGV.setText("");
            txt_TenGV.setText("");
            txt_SDTGV.setText("");
            txt_EMGV.setText("");
            txt_DCGV.setText("");
            txt_NSGV.setText("");
            sl_TT.setSelectedItem("Còn Hoạt Động");
            btn_SuaGV.setEnabled(false);
            btn_XoaGV.setEnabled(false);
            txt_MaGV.setEnabled(true);
            btn_ThemGV.setEnabled(true);
        } else
        JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đầy đủ thông tin!");

    }//GEN-LAST:event_btn_SuaGVActionPerformed

