    private void jPanel2PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jPanel2PropertyChange
        btn_SuaGV.setEnabled(false);
        btn_XoaGV.setEnabled(false);
        GiaoVienBLL GVGLL = new GiaoVienBLL();
        listGV = GVGLL.ShowDSGV();
        model = (DefaultTableModel) tbl_GV.getModel();
        model.setColumnIdentifiers(new Object[]{
            "Mã Giáo Viên","Tên Giáo Viên", "Tên Môn Học", "Năm Sinh", "Số Điện Thoại", "Địa Chỉ", "Email", "Tình Trạng"
        });
        int rowCount = model.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--)
        model.removeRow(i);
        listMH = new MonHocBLL().ShowDSMH();
        sl_MH.removeAllItems();
        for(MonHoc MH: listMH) {
            sl_MH.addItem(MH.getTenMH());
        }
        sl_TT.removeAllItems();
        sl_TT.addItem("Còn Hoạt Động");
        sl_TT.addItem("Ngừng Hoạt Động");
        showTableGV(listGV);
    }//GEN-LAST:event_jPanel2PropertyChange


