    public void showTableMH(ArrayList<MonHoc> list) {
        for(MonHoc mh:list) {
            model.addRow(new Object[]{
                mh.getMaMH(), mh.getTenMH()
            });
        }
    }
    
    public void showTableGV(ArrayList<GiaoVien> list) {
        for(GiaoVien GV:list) {
            String TT = "Còn Hoạt Động";
            if(GV.getTinhTrang()==0)
                TT = "Ngừng Hoạt Động";    
            model.addRow(new Object[]{
                GV.getMaGV(), GV.getTenGV(), GV.getTenMH(), GV.getNamSinh(), GV.getSDT(), GV.getDiaChi(), GV.getEmail(), TT
            });
        }
    }
    
    
